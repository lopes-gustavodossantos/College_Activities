<?php

function somaDois(float $valor1, float $valor2) {
    return ($valor1 + $valor2);
}

function subtracaoDois(float $valor1, float $valor2) {
    return ($valor1 - $valor2);
}

function multiplicacaoDois(float $valor1, float $valor2) {
    return ($valor1 * $valor2);
}

function divisaoDois(float $valor1, float $valor2) {
    return ($valor1 / $valor2);
}

?>